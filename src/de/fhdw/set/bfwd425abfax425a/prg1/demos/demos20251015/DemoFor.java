package de.fhdw.set.bfwd425abfax425a.prg1.demos.demos20251015;

public class DemoFor {

    public static void main(String[] args) {
        for (int i=1; i<=15; i+=2) {
            System.out.println(i);
        }

        int i = 1;
        while (i<=15) {
            System.out.println(i);
            i+=2;
        }

    }
}
